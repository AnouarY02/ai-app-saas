// Minimal API client for frontend/backend shared use
import type { LoginRequest, LoginResponse, LogoutRequest, LogoutResponse, UpdateProfileRequest, UserProfile, ApiError } from '../types/api';

export async function apiPost<TReq, TRes>(url: string, body: TReq, token?: string): Promise<TRes> {
  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
    },
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const error: ApiError = await res.json();
    throw error;
  }
  return res.json();
}

