// User controller
import { Request, Response } from 'express';
import { z } from 'zod';
import { userRepository } from '../repositories/userRepository';
import { hashPassword } from '../utils/passwordUtils';
import { AuthRequest } from '../middleware/authMiddleware';

const updateProfileRequestSchema = z.object({
  name: z.string().optional(),
  password: z.string().min(6).optional(),
});

export const userController = {
  async getProfile(req: Request, res: Response) {
    const authReq = req as AuthRequest;
    if (!authReq.user) {
      return res.status(401).json({ error: 'Unauthorized' });
    }
    const user = await userRepository.findById(authReq.user.id);
    if (!user) {
      return res.status(404).json({ error: 'User not found' });
    }
    const { id, email, name, createdAt, updatedAt } = user;
    res.json({ id, email, name, createdAt, updatedAt });
  },

  async updateProfile(req: Request, res: Response) {
    const authReq = req as AuthRequest;
    if (!authReq.user) {
      return res.status(401).json({ error: 'Unauthorized' });
    }
    const parse = updateProfileRequestSchema.safeParse(req.body);
    if (!parse.success) {
      return res.status(400).json({ error: 'Invalid profile update data' });
    }
    const { name, password } = parse.data;
    const update: any = {};
    if (name !== undefined) update.name = name;
    if (password) {
      update.passwordHash = await hashPassword(password);
    }
    const user = await userRepository.update(authReq.user.id, update);
    if (!user) {
      return res.status(404).json({ error: 'User not found' });
    }
    const { id, email, name: updatedName, createdAt, updatedAt } = user;
    res.json({ id, email, name: updatedName, createdAt, updatedAt });
  },
};

