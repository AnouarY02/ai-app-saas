import React, { createContext, useContext, useEffect, useState } from 'react';
import api from '../utils/api';

// Shared types (simulate import from shared)
export interface UserProfile {
  id: string;
  email: string;
  name: string;
  createdAt: string;
  updatedAt: string;
}

interface AuthContextType {
  user: UserProfile | null;
  isAuthenticated: boolean;
  loading: boolean;
  login: (data: { email: string; password: string }) => Promise<void>;
  signup: (data: { email: string; password: string; name?: string }) => Promise<void>;
  logout: () => Promise<void>;
  updateProfile: (data: { name?: string; password?: string }) => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [user, setUser] = useState<UserProfile | null>(null);
  const [loading, setLoading] = useState(true);

  const fetchProfile = async () => {
    try {
      const res = await api.get<UserProfile>('/api/users/me');
      setUser(res.data);
    } catch {
      setUser(null);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    fetchProfile();
    // eslint-disable-next-line
  }, []);

  const login = async (data: { email: string; password: string }) => {
    setLoading(true);
    try {
      const res = await api.post<{ user: UserProfile; token: string }>('/api/auth/login', data);
      setUser(res.data.user);
    } catch (err: any) {
      throw err.response?.data?.message ? new Error(err.response.data.message) : err;
    } finally {
      setLoading(false);
    }
  };

  const signup = async (data: { email: string; password: string; name?: string }) => {
    setLoading(true);
    try {
      const res = await api.post<{ user: UserProfile; token: string }>('/api/auth/signup', data);
      setUser(res.data.user);
    } catch (err: any) {
      throw err.response?.data?.message ? new Error(err.response.data.message) : err;
    } finally {
      setLoading(false);
    }
  };

  const logout = async () => {
    setLoading(true);
    try {
      await api.post('/api/auth/logout', {});
      setUser(null);
    } catch {
      // ignore
    } finally {
      setLoading(false);
    }
  };

  const updateProfile = async (data: { name?: string; password?: string }) => {
    setLoading(true);
    try {
      const res = await api.put<UserProfile>('/api/users/me', data);
      setUser(res.data);
    } catch (err: any) {
      throw err.response?.data?.message ? new Error(err.response.data.message) : err;
    } finally {
      setLoading(false);
    }
  };

  const value: AuthContextType = {
    user,
    isAuthenticated: !!user,
    loading,
    login,
    signup,
    logout,
    updateProfile,
  };

  return <AuthContext.Provider value={value}>{children}</AuthContext.Provider>;
};

export const useAuth = () => {
  const ctx = useContext(AuthContext);
  if (!ctx) throw new Error('useAuth must be used within AuthProvider');
  return ctx;
};

